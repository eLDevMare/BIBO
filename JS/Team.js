document.querySelector('.select-v1').addEventListener('click', function() {
    const elementsV1 = document.querySelectorAll('.img-v1');
    elementsV1.forEach(element => {
        element.style.display = 'block';
        setTimeout(() => {
            element.style.opacity = '1';
        }, 400); // Waktu 1 detik
    });

    const elementsV2 = document.querySelectorAll('.text-v1');
    elementsV2.forEach(element => {
        element.style.display = 'block';
        setTimeout(() => {
            element.style.opacity = '1';
        }, 200); // Waktu 1 detik
    });

    const elementsV3 = document.querySelectorAll('.img-original, .text-original, .text-v2, .img-v2, .text-v3, .img-v3, .text-v4, .img-v4, .text-v5, .img-v5, .img-v4, .text-v5, .img-v5, .img-v6, .text-v6, .text-v7, .img-v7, .text-v8, .img-v8');
    elementsV3.forEach(element => {
        element.style.display = 'none';
        element.style.opacity = '0';
    });
});


