document.querySelector('.back-game-team').addEventListener('click', function () {
    const back1 = document.querySelectorAll('.select-team');
    const back2 = document.querySelectorAll('.home, .team-group-icon, .key-icon');

    back1.forEach(element => {
        element.style.display = 'none';
        element.style.opacity = '0';
    })

    back2.forEach( element => {
        element.style.display = 'block';
        setTimeout(() => {
            element.style.opacity = '1'
        }, 40)
    })

})




